/*Дано оцінки студента з n предметів у 100 бальній шкалі ECTS. Вивести
номери предметів, з яких у учня відмінні оцінки (>=90 балів)?
*/
let n = parseInt(prompt("Введіть кількість предметів: "));
let arr = [];
for (let i=0; i<n; i++){
    arr[i] = parseInt(prompt(`Введіть оцінку ${i+1} предмета`, "0"));
}
for (let i=0; i<n; i++){
    if(arr[i]>=60){
        alert(` Оцінка ${arr[i]} предмета ${i+1} є відмінною` );
    }
}