/*3. Дано масив імен студентів,. З’ясувати скільки разів зустрічається вказане користувачем ім’я, наприклад «Юрій».*/

let k = parseInt(prompt("Введіть кількість імен: "));
let arr = [];
let n = 0;
for (let i=0; i<k; i++){
    el = (prompt(`Введіть ім’я: `));
    arr.push(el);
}
for (let i=0; i<k; i++){
    if(arr[i]== "Юрій"){
        n += 1
    }
}
alert(`Імена студентів: ${arr} ` );
alert(` Юрій зустрічався ${n} раз(и)` );

